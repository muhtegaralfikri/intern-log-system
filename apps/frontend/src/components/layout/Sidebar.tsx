'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useAuthStore } from '@/stores/authStore';
import { useThemeStore } from '@/stores/themeStore';

const menuItems = [
  { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
  { name: 'Absensi', href: '/dashboard/attendance', icon: ClockIcon },
  { name: 'Aktivitas', href: '/dashboard/activities', icon: ClipboardIcon },
  { name: 'Skills', href: '/dashboard/skills', icon: StarIcon },
  { name: 'Badges', href: '/dashboard/badges', icon: BadgeIcon },
  { name: 'Mood', href: '/dashboard/mood', icon: EmojiIcon },
  { name: 'Laporan', href: '/dashboard/reports', icon: ChartIcon },
  { name: 'Profil', href: '/dashboard/profile', icon: UserIcon },
  { name: 'Pengaturan', href: '/dashboard/settings', icon: SettingsIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { user, logout } = useAuthStore();
  const { theme, setTheme } = useThemeStore();

  const isAdmin = user?.role === 'ADMIN';
  const isSupervisor = user?.role === 'SUPERVISOR';

  const toggleTheme = () => {
    setTheme(theme === 'light' ? 'dark' : 'light');
  };

  const getThemeIcon = () => {
    return theme === 'dark' ? <MoonIcon className="w-5 h-5" /> : <SunIcon className="w-5 h-5" />;
  };

  const getThemeLabel = () => {
    return theme === 'dark' ? 'Gelap' : 'Terang';
  };

  return (
    <aside className="hidden lg:flex fixed left-0 top-0 h-full w-64 bg-white dark:bg-slate-900 border-r border-gray-200 dark:border-slate-700 flex-col transition-colors">
      <div className="p-6 border-b border-gray-200 dark:border-slate-700">
        <h1 className="text-xl font-bold text-gray-900 dark:text-white">Intern Log</h1>
        <p className="text-sm text-gray-500 dark:text-slate-400 mt-1">Sistem Pencatatan Intern</p>
      </div>

      <nav className="flex-1 p-4 overflow-y-auto scrollbar-custom">
        <ul className="space-y-1">
          {menuItems.map((item) => {
            const isActive = pathname === item.href || 
              (item.href !== '/dashboard' && pathname.startsWith(item.href));
            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={`flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-blue-50 dark:bg-blue-900/30 text-blue-600 dark:text-blue-400'
                      : 'text-gray-600 dark:text-slate-300 hover:bg-gray-50 dark:hover:bg-slate-800 hover:text-gray-900 dark:hover:text-white'
                  }`}
                >
                  <item.icon className="w-5 h-5" />
                  {item.name}
                </Link>
              </li>
            );
          })}
        </ul>

        {isAdmin && (
          <div className="mt-6 pt-4 border-t border-gray-200 dark:border-slate-700">
            <p className="px-4 text-xs font-semibold text-gray-400 dark:text-slate-500 uppercase tracking-wider mb-2">
              Admin
            </p>
            <Link
              href="/admin"
              className={`flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                pathname.startsWith('/admin')
                  ? 'bg-purple-50 dark:bg-purple-900/30 text-purple-600 dark:text-purple-400'
                  : 'text-gray-600 dark:text-slate-300 hover:bg-gray-50 dark:hover:bg-slate-800 hover:text-gray-900 dark:hover:text-white'
              }`}
            >
              <AdminIcon className="w-5 h-5" />
              Admin Panel
            </Link>
          </div>
        )}

        {isSupervisor && (
          <div className="mt-6 pt-4 border-t border-gray-200 dark:border-slate-700">
            <p className="px-4 text-xs font-semibold text-gray-400 dark:text-slate-500 uppercase tracking-wider mb-2">
              Supervisor
            </p>
            <Link
              href="/supervisor"
              className={`flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                pathname.startsWith('/supervisor')
                  ? 'bg-purple-50 dark:bg-purple-900/30 text-purple-600 dark:text-purple-400'
                  : 'text-gray-600 dark:text-slate-300 hover:bg-gray-50 dark:hover:bg-slate-800 hover:text-gray-900 dark:hover:text-white'
              }`}
            >
              <SupervisorIcon className="w-5 h-5" />
              Panel Supervisor
            </Link>
          </div>
        )}

        <div className="mt-6 pt-4 border-t border-gray-200 dark:border-slate-700">
          <button
            onClick={toggleTheme}
            className="w-full flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium text-gray-600 dark:text-slate-300 hover:bg-gray-50 dark:hover:bg-slate-800 hover:text-gray-900 dark:hover:text-white transition-colors"
          >
            {getThemeIcon()}
            <span>Mode: {getThemeLabel()}</span>
          </button>
        </div>
      </nav>

      <div className="p-4 border-t border-gray-200 dark:border-slate-700">
        <div className="flex items-center gap-3 mb-3">
          <div className="w-10 h-10 rounded-full bg-blue-100 dark:bg-blue-900/50 flex items-center justify-center">
            <span className="text-blue-600 dark:text-blue-400 font-semibold">
              {user?.name?.charAt(0).toUpperCase() || 'U'}
            </span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-gray-900 dark:text-white truncate">{user?.name}</p>
            <p className="text-xs text-gray-500 dark:text-slate-400 truncate">{user?.email}</p>
          </div>
        </div>
        <button
          onClick={logout}
          className="w-full flex items-center justify-center gap-2 px-4 py-2 text-sm text-red-600 dark:text-red-400 hover:bg-red-50 dark:hover:bg-red-900/20 rounded-lg transition-colors"
        >
          <LogoutIcon className="w-4 h-4" />
          Keluar
        </button>
      </div>
    </aside>
  );
}

